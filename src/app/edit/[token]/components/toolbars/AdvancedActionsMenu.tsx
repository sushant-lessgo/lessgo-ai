import React, { forwardRef, useEffect, useRef } from 'react';
import { useAdvancedActionsMenu } from '@/hooks/useAdvancedActionsMenu';

export interface AdvancedActionItem {
  id: string;
  label: string;
  icon: string;
  handler: () => void;
  disabled?: boolean;
  group?: string;
  shortcut?: string;
}

export interface AdvancedActionGroup {
  id: string;
  label?: string;
  actions: AdvancedActionItem[];
}

interface AdvancedActionsMenuProps {
  actions: AdvancedActionItem[];
  triggerElement: HTMLElement;
  onClose: () => void;
  toolbarType: 'section' | 'element' | 'text' | 'form' | 'image';
  isVisible: boolean;
}

export const AdvancedActionsMenu = forwardRef<HTMLDivElement, AdvancedActionsMenuProps>(
  ({ actions, triggerElement, onClose, toolbarType, isVisible }, ref) => {
    const menuRef = useRef<HTMLDivElement>(null);
    
    const {
      position,
      groupedActions,
      updatePosition,
      handleClickOutside,
      handleKeyNavigation,
    } = useAdvancedActionsMenu({
      triggerElement,
      isVisible,
      onClose,
      actions,
      toolbarType,
    });

    // Combine refs
    const combinedRef = (node: HTMLDivElement) => {
      (menuRef as any).current = node;
      if (typeof ref === 'function') {
        ref(node);
      } else if (ref) {
        (ref as any).current = node;
      }
    };

    // Handle click outside
    useEffect(() => {
      if (isVisible) {
        document.addEventListener('mousedown', handleClickOutside);
        document.addEventListener('keydown', handleKeyNavigation);
        
        return () => {
          document.removeEventListener('mousedown', handleClickOutside);
          document.removeEventListener('keydown', handleKeyNavigation);
        };
      }
    }, [isVisible, handleClickOutside, handleKeyNavigation]);

    // Update position when visible
    useEffect(() => {
      if (isVisible && triggerElement) {
        updatePosition();
      }
    }, [isVisible, triggerElement, updatePosition]);

    if (!isVisible) return null;

    return (
      <div
        ref={combinedRef}
        className="fixed z-60 bg-white border border-gray-200 rounded-lg shadow-lg min-w-[200px] max-w-[280px] py-2"
        style={{
          left: position.x,
          top: position.y,
          transform: position.transform,
        }}
        role="menu"
        aria-orientation="vertical"
        tabIndex={-1}
      >
        {/* Arrow indicator */}
        {position.arrow && (
          <div
            className={`absolute w-2 h-2 bg-white border-gray-200 transform rotate-45 ${
              position.arrow.side === 'top' ? 'border-b border-r -top-1' :
              position.arrow.side === 'bottom' ? 'border-t border-l -bottom-1' :
              position.arrow.side === 'left' ? 'border-r border-b -left-1' :
              'border-l border-t -right-1'
            }`}
            style={{
              left: position.arrow.side === 'left' || position.arrow.side === 'right' ? undefined : position.arrow.offset,
              top: position.arrow.side === 'top' || position.arrow.side === 'bottom' ? undefined : position.arrow.offset,
            }}
          />
        )}

        {/* Menu content */}
        <div className="relative">
          {groupedActions.map((group, groupIndex) => (
            <React.Fragment key={group.id}>
              {/* Group divider */}
              {groupIndex > 0 && (
                <div className="h-px bg-gray-200 my-1" />
              )}
              
              {/* Group label */}
              {group.label && (
                <div className="px-3 py-1 text-xs font-medium text-gray-500 uppercase tracking-wide">
                  {group.label}
                </div>
              )}

              {/* Group actions */}
              {group.actions.map((action) => (
                <button
                  key={action.id}
                  onClick={() => {
                    if (!action.disabled) {
                      action.handler();
                      onClose();
                    }
                  }}
                  disabled={action.disabled}
                  className={`
                    flex items-center w-full px-3 py-2 text-sm text-left transition-colors
                    ${action.disabled 
                      ? 'text-gray-400 cursor-not-allowed' 
                      : 'text-gray-700 hover:bg-gray-50 hover:text-gray-900 focus:bg-gray-50 focus:text-gray-900'
                    }
                    focus:outline-none
                  `}
                  role="menuitem"
                  tabIndex={action.disabled ? -1 : 0}
                >
                  <div className="flex items-center justify-center w-4 h-4 mr-3 flex-shrink-0">
                    <AdvancedActionIcon icon={action.icon} />
                  </div>
                  
                  <span className="flex-1 truncate">{action.label}</span>
                  
                  {action.shortcut && (
                    <span className="ml-2 text-xs text-gray-400 font-mono">
                      {action.shortcut}
                    </span>
                  )}
                </button>
              ))}
            </React.Fragment>
          ))}
        </div>
      </div>
    );
  }
);

AdvancedActionsMenu.displayName = 'AdvancedActionsMenu';

